export const useGetPosts = () => {
  return useInfiniteQuery({
    queryKey: [QUERY_KEYS.GET_INFINITE_POSTS],
    queryFn: getInfinitePosts,
    getNextPageParam: (lastPage) =>{
      if(lastPage && lastPage.documents.length === 0) return null ;
       const lastId = lastPage?.documents[lastPage?.documents.length - 1].$id

       return lastId
    }
  });
};



export const useSearchPosts = (searchTerm: string) => {
  return useQuery({
    queryKey: [QUERY_KEYS.SEARCH_POSTS, searchTerm],
    queryFn: () => getSearchPosts(searchTerm),
    enabled: !!searchTerm.trim(), // Ensure query only runs for non-empty searchTerm
  });
};


export const useGetUserPosts = (userId?: string, limit = 10) => {
  return useInfiniteQuery({
    queryKey: ["getUserPosts", userId, limit],
    queryFn: ({ pageParam = "" }) => getUserPosts(userId, limit, pageParam), // ✅ Pass `pageParam` correctly
    enabled: !!userId,
    initialPageParam: "", // ✅ REQUIRED: This was missing
    getNextPageParam: (lastPage) =>
      lastPage?.documents?.length ? lastPage.documents.slice(-1)[0]?.$id : null, // ✅ Safe pagination
  });
};

